$(document).ready(function () {
  /*웹페이지 열었을 때*/ $("#img1").show();
  $("#img2").hide();
  /*img1을 클릭했을 때
img2를 보여줌*/ $("#img1").click(function () {
    $("#img1").hide();
    $("#img2").show();
  });
  /*img2를 클릭했을 때
img1을 보여줌*/ $("#img2").click(function () {
    $("#img1").show();
    $("#img2").hide();
  });
});

$(function () {
  let num = 0;

  $("#pic1").click(function () {
    if (num == 0) {
      $(this).attr("src", "../data/img/star2.png");
      num = 1;
    } else {
      $(this).attr("src", "../data/img/star1.png");
      num = 0;
    }
  });
});

