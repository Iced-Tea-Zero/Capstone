$(function () {
  let num = 0;

  $("#pic1").click(function () {
    if (num == 0) {
      $(this).attr("src", "../data/img/star2.png");
      num = 1;
    } else {
      $(this).attr("src", "../data/img/star1.png");
      num = 0;
    }
  });
});

